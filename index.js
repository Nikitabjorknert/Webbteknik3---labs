 function createLabCard(lab) {
  // Skapa huvudcontainern
  const card = document.createElement('div');
  card.classList.add('lab-card');

  card.innerHTML = `
    <h3>${lab.title}</h3>
    <div>
     <span class="week">Vecka ${lab.week}</span>
    </div>
     <div class="lab-meta">
      ${lab.showLink ? `<a href ${lab.link}>Öppna labb</a>` : ''} 
     <span class="status ${lab.completed ? 'completed' : ''}">
      ${lab.completed ? 'Completed' : ''}</span>
    </div>
    <p>${lab.description}</p>
    <br>
  `;
    
  return card;
}

function renderLabs(labsToShow) {
  const container = document.getElementById('labs-container');

  // Rensa befintligt innehåll
  container.innerHTML = '';

  // Skapa och lägg till kort för varje labb
  for (const lab of labsToShow) {
    const card = createLabCard(lab);
    container.append(card);
  }
}

// Rendera vid sidladdning
renderLabs(labs);


